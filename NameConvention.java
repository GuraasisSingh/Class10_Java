
public class NameConvention
{

    /**
     * Class Name should have all first letters in capitals(PascalCase)
     * like 
     * StudentsFullName
     * 
     * Constants name should have all capital letters in it
     * like
     * ARRAY_SIZE
     * NUM_STUDENTS
     * final float PI =3.1414;
     * 
     * Variable/Arrays/Methods Name should have first word small and first letter of second alphabet small(camelCase)
     * like
     * studentFullName
     */ 
}
